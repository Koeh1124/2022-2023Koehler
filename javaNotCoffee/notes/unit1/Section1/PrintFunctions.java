public class PrintFunctions {
    public static void print(String s){
        System.out.println(s);
    }
    public static void print(int s){
        System.out.println(s);
    }
    public static void print(double s){
        System.out.println(s);
    }
    public static void print(Boolean s){
        System.out.println(s);
    }
    
}
